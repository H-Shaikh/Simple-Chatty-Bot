import java.util.*

fun main(args: Array<String>) {
    val scanner = Scanner(System.`in`)
    val n = scanner.nextInt()
    var fix = 0
    var ready = 0
    var rejects = 0
    val larger = 1
    val smaller = -1
    val perfect = 0
    repeat(n){
       n == larger)
        fix += larger


        n == smaller

            rejects += smaller

        n == perfect
            ready += perfect

        }
    }
        
    
    print(fix)
    print(rejects)
    print(ready)
}
